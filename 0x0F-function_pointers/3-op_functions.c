#include "3-calc.h"

/**
 * op_add - add numbers
 * @a: (int)
 * @b: (int)
 *
 * Description: This function make the sum
 * of two numbers
 * Return: An integer
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subdvise number
 * @a: (int)
 * @b: (int)
 *
 * Description: This function give the defference
 * of a and b
 * Return: An integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply two numbers
 * @a: (int)
 * @b: (int)
 *
 * Description: Multiply two numbers
 * Return: An integerr
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divise two numbers
 * @a: (int)
 * @b: (int)
 *
 * Description: Divise two numbers
 * Return: An integer
 */

int op_div(int a, int b)
{
	return (a /  b);
}


/**
 * op_mod - modulo of two numbers
 * @a: (int)
 * @b: (int)
 *
 * Description: This function return the
 * modulo of two numbers
 * Return: An integer
 */

int op_mod(int a, int b)
{
	return (a % b);
}
